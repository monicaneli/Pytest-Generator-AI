def soma(a, b):
    """Retorna a soma de dois números."""
    return a + b

def subtrai(a, b):
    """Retorna a subtração de dois números."""
    return a - b

def multiplica(a, b):
    """Retorna a multiplicação de dois números."""
    return a * b

def divide(a, b):
    """Retorna a divisão de dois números. Lança ZeroDivisionError se b == 0."""
    return a / b
